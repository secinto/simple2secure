package com.simple2secure.api.model;

import com.simple2secure.api.dbo.GenericDBObject;

public class WidgetProperties extends GenericDBObject{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 6253109541875693926L;
	
	private String widgetId;
	private int top;
	private int left;
	private int height;
	private int width;
	private String bgClass;
	private String icon;
	private String count;
	private String label;
	
	public WidgetProperties() {
		
	}
	
	public WidgetProperties(String widgetId, int top, int left, int height, int width, String bgClass, String icon, String count,
			String label) {
		super();
		this.widgetId = widgetId;
		this.top = top;
		this.left = left;
		this.height = height;
		this.width = width;
		this.bgClass = bgClass;
		this.icon = icon;
		this.count = count;
		this.label = label;
	}
	
	public String getWidgetId() {
		return widgetId;
	}

	public void setWidgetId(String widgetId) {
		this.widgetId = widgetId;
	}

	public int getTop() {
		return top;
	}
	public void setTop(int top) {
		this.top = top;
	}
	public int getLeft() {
		return left;
	}
	public void setLeft(int left) {
		this.left = left;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public String getBgClass() {
		return bgClass;
	}
	public void setBgClass(String bgClass) {
		this.bgClass = bgClass;
	}
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
}
